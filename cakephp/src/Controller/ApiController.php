     * @throws \Cake\View\Exception\MissingTemplateException In debug mode.
     */
    public function index()
    {
		$url = "https://weather.tsukumijima.net/api/forecast/city/130010";
		$json = file_get_contents($url);
		$arr = json_decode($json,true);
		$this->set("arr",$arr);

    }


}

